function DBTimeToDate( value ) {
    if( value == null ) {
        return null;
    }
    return new Date(value);
}
exports.DBTimeToDate = DBTimeToDate;

function DateToDBTime( value ) {
    var year = value.getFullYear();
    var month = value.getMonth() + 1;
    var date = value.getDate();
    var hour = value.getHours();
    var min = value.getMinutes();
    var sec = value.getSeconds();

    return year + '-' + month + '-' + date + ' ' + hour + ':' + min + ':' + sec;
}
exports.DateToDBTime = DateToDBTime;

// Date 데이터를 Object형태로 변환
function DateToObject( value ) {
	var ret = {};
    ret.year = value.getFullYear();
    ret.month = value.getMonth() + 1;
    ret.date = value.getDate();
    ret.hour = value.getHours();
    ret.min = value.getMinutes();
    ret.sec = value.getSeconds();

    return ret;
}
exports.DateToObject = DateToObject;

function IntToBool( value ) {
    return value == 0 ? false : true;
}
exports.IntToBool = IntToBool;

function BoolToInt( value ) {
    return value == false ? 0 : 1;
}
exports.BoolToInt = BoolToInt;